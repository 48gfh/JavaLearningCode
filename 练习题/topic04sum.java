public class topic04sum {
    public static void main(String[] args) {
        // 练习题：定义两个整数，计算这两个整数加减乘除运算的结果，并把结果打印到控制台
        int num1 = 15;
        int num2 = 34;
        System.out.println("num1+num2="+(num1+num2));
        System.out.println("num*num2="+(num1*num2));
        System.out.println("num/num2="+(num1/num2));
    }
}
